function lk(nums: number[]): number {
    let max1 = -Infinity, max2 = -Infinity
    let min1 = Infinity, min2 = Infinity

    for (let i = 0; i < nums.length; i++) {
        if(nums[i] > max1) {
            max2 = max1
            max1 = nums[i]
        }
        else if(nums[i] > max2) {
            max2 = nums[i]
        }

        if(nums[i] < min1) {
            min2 = min1
            min1 = nums[i]
        }
        else if(nums[i] < min2) {
            min2 = nums[i]
        }
    }

    return max1 * max2 - min1 * min2
}

