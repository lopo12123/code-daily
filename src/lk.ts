function lk(arr: number[]): number {
    arr.sort((a, b) => a - b)

    let sum = 0
    for (let i = arr.length * 0.05; i <= arr.length * 0.95 - 1; i++) {
        sum += arr[i]
    }

    return sum / (arr.length * 0.9)
}

