function lk(nums: number[]): number {
    let max_sum = nums[0]
    let sum_now = nums[0]

    for (let i = 1; i < nums.length; i++) {
        if(nums[i] <= nums[i - 1]) {
            max_sum = Math.max(max_sum, sum_now)
            sum_now = 0
        }
        sum_now += nums[i]
    }

    return Math.max(max_sum, sum_now)
}

console.log(lk([ 10, 20, 30, 5, 10, 50 ]))  // 65
console.log(lk([ 10, 20, 30, 40, 50 ]))  // 150

