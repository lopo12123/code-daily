function lk(s1: string, s2: string): boolean {
    let diff_count = 0
    let diff_ch = [ '', '' ]

    for (let i = 0; i < s1.length; i++) {
        if(s1[i] !== s2[i]) {
            if(diff_count === 0) {
                diff_ch = [ s1[i], s2[i] ]
                diff_count += 1
            }
            else if(diff_count === 1) {
                if(diff_ch[0] !== s2[i] || diff_ch[1] !== s1[i]) return false
                else diff_count += 1
            }
            else return false
        }
    }

    return diff_count === 0 || diff_count === 2
}

