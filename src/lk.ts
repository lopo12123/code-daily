function lk(s: string): number {
    const zero_one: [ number, number ] = [0, 0]
    for (let i = 0; i < s.length; i ++) {
        // @ts-ignore
        zero_one[s[i]] += 1
    }
    // @ts-ignore
    let score = (s[0] === '0') + zero_one[1] - (s[0] === '1')

    let max_score = score
    for (let i = 1; i < s.length - 1; i ++) {
        score += (s[i] === '0' ? 1 : -1)
        max_score = Math.max(max_score, score)
    }
    return max_score
}

