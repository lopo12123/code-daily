function lk(nums: number[]): number {
    while (nums.length > 1) {
        const newNum: number[] = []
        for (let i = 0; i < nums.length / 2; i ++) {
            newNum[i] = i % 2 === 0
                ? Math.min(nums[2 * i], nums[2 * i + 1])
                : Math.max(nums[2 * i], nums[2 * i  +1])
        }
        nums = newNum
    }
    return nums[0]
}

