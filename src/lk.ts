function lk(nums: number[], k: number): number {
    const in_order = nums.sort((a, b) => a - b)

    let part = 0
    let start = in_order[0]
    for (let i = 0; i < in_order.length; i++) {
        if(in_order[i] - start > k) {
            console.log(in_order[i], 'break')
            part += 1
            start = in_order[i]
        }
    }

    return part + 1
}

