function lk(s: string): string {
    const res_arr: string[] = new Array(s.length)

    let l = 0, r = s.length - 1

    while (l <= r) {
        if(!/[a-zA-Z]/.test(s[l])) {
            res_arr[l] = s[l]
            l += 1
        }
        else if(!/[a-zA-Z]/.test(s[r])) {
            res_arr[r] = s[r]
            r -= 1
        }
        else {
            res_arr[l] = s[r]
            res_arr[r] = s[l]
            l += 1
            r -= 1
        }
    }

    return res_arr.join('')
}

