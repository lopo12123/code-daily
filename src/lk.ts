function lk(arr: number[]): boolean {
    let count = 0
    for (let i = 0; i < arr.length; i++) {
        if(arr[i] % 2 === 0) count = 0
        else if(count === 2) return true
        else {
            count += 1
        }
    }
    return false
}

console.log(lk([ 2, 6, 4, 1 ]))
console.log(lk([ 1, 2, 34, 3, 4, 5, 7, 23, 12 ]))

