function lk(path: string): boolean {
    const pass = new Set()
    let curr = [0, 0]

    pass.add((curr.join('-')))
    for (let i = 0; i < path.length; i ++) {
        switch (path[i]){
            case 'N':
                curr[0] += 1
                break
            case 'S':
                curr[0] -= 1
                break
            case 'E':
                curr[1] += 1
                break
            case 'W':
                curr[1] -= 1
                break
        }
        if(pass.has(curr.join('-'))) return true
        else pass.add((curr.join('-')))
    }

    return false
}

