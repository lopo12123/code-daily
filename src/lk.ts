function lk(root: TreeNode | null, low: number, high: number): number {
    let sum = 0

    const dfs_sum = (sub: TreeNode | null) => {
        if(sub === null) return
        else {
            dfs_sum(sub.left)
            if(sub.val >= low && sub.val <= high) sum += sub.val
            if(sub.val <= high) dfs_sum(sub.right)
        }
    }

    dfs_sum(root)

    return sum
}

