function lk(m: number, n: number, ops: number[][]): number {
    let [ m_min, n_min ] = ops[0] ?? [ m, n ]
    for (let i = 0; i < ops.length; i++) {
        m_min = Math.min(m_min, ops[i][0])
        n_min = Math.min(n_min, ops[i][1])
    }
    return m_min * n_min
}

