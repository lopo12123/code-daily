function lk(logs: number[][]): number {
    const num_of_year: number[] = new Array(101).fill(0)

    for (let log of logs) {
        num_of_year[log[0] - 1950] += 1
        num_of_year[log[1] - 1950] -= 1
    }

    let max = 0, maxYear = 0
    let curr = 0

    num_of_year.forEach((delta, year) => {
        curr += delta
        if(curr > max) {
            max = curr
            maxYear = year
        }
    })

    return maxYear + 1950
}

