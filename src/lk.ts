function lk(num: number): number {
    let pre = Math.floor(num / 10)
    let count = (pre * 5 - 1)

    let self_sum = 0
    while (pre > 0) {
        self_sum += pre % 10
        pre = Math.floor(pre / 10)
    }

    for (let i = self_sum % 2; i <= num % 10; i += 2) {
        count += 1
    }

    return count
}

