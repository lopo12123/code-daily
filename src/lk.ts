function lk(lowLimit: number, highLimit: number): number {
    const box: number[] = []

    for (let i = lowLimit; i <= highLimit; i++) {
        let box_id = 0, p = i
        while (p > 0) {
            box_id += p % 10
            p = Math.floor(p / 10)
        }
        box[box_id] = (box[box_id] ?? 0) + 1
    }

    let max = 0
    box.forEach((num_of_ball) => {
        max = Math.max(num_of_ball, max)
    })
    return max
}

