function lk(n: number): number {
    const max_len = Math.sqrt(2 * n + 1)
    // 一个数字n的情况必定存在
    let count = 0

    let num_of_queue = 1
    while (num_of_queue < max_len) {
        if((n - (num_of_queue - 1) * num_of_queue / 2) % num_of_queue === 0) {
            count += 1
        }

        num_of_queue += 1
    }

    return count
}

