function lk(n: number): number {
    const types: number[] = [1, 1, 2, 5]

    if(n <= 3) return types[n]

    // f(n) = f(n - 1) * f(0) + f(n - 2) * f(1) + ... + f(1) * f(n - 2) + f(0) * f(n - 1)
    for (let i = 4; i <= n; i ++) {
        types[i] = 0
        for (let t = 0; t <= i - 1; t ++) {
            types[i] += types[t] * types[i - 1 - t]
        }
    }
    return types[n]
}


