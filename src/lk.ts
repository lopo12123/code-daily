function lk(n: number): number {
    // 偶数idx nums[idx / 2]
    // 奇数idx nums[(idx - 1) / 2] + nums[(idx + 1) / 2]

    if(n === 0) return 0
    else if(n === 1) return 1

    const nums: number[] = [ 0, 1 ]
    let max = 0
    for (let i = 2; i <= n; i++) {
        if(i % 2 === 0) {
            nums[i] = nums[i / 2]
        }
        else {
            nums[i] = nums[(i - 1) / 2] + nums[(i + 1) / 2]
        }
        max = Math.max(max, nums[i])
    }

    return max
}

