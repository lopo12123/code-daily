function lk(n: number): number {
    let count = 0
    for (let a = 0; a <= n; a++) {
        for (let b = a + 1; b <= n; b++) {
            for (let c = b + 1; c <= n; c++) {
                if(a * a + b * b === c * c) count += 1
            }
        }
    }
    return count * 2
}

