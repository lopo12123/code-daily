function lk(s: string): boolean {
    const ifTrue = (start_l: number, start_r: number) => {
        while (start_l < start_r) {
            if(s[start_l] !== s[start_r]) return false
            else {
                start_l += 1
                start_r -= 1
            }
        }
        return true
    }

    let [l, r] = [0, s.length - 1]
    while (l < r) {
        if(s[l] === s[r]) {
            l += 1
            r -= 1
        }
        else {
            return ifTrue(l + 1, r) || ifTrue(l, r - 1)
        }
    }

    return true
}

