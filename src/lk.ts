function lk(root: TreeNode): TreeNode | null {
    const pre = new TreeNode(0)
    let p = pre

    const mid_root = (sub: TreeNode | null) => {
        if(sub === null) return
        else {
            mid_root(sub.left)
            p.right = new TreeNode(sub.val)
            p = p.right
            mid_root(sub.right)
        }
    }
    mid_root(root)

    return pre.right
}

