function lk(strs: string[]): number {
    let count = 0
    const x_len = strs[0].length, y_len = strs.length

    if(y_len === 1) return 0

    for (let x = 0; x < x_len; x ++) {
        for (let y = 1; y < y_len; y ++) {
            if(strs[y][x] < strs[y - 1][x]) {
                count += 1
                break
            }
        }
    }

    return count
}

